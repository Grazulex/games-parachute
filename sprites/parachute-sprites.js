  draw: function(ctx, spriteName, x, y, scale = 1, color = '#1a1a0f') {
    const parts = spriteName.split('.');
    let sprite = this[parts[0]];
    if (parts[1]) sprite = sprite[parts[1]];
    
    if (!sprite || !sprite.data) {
      console.error('Sprite non trouvé:', spriteName);
      return;
    }
    
    ctx.fillStyle = color;
    sprite.data.forEach((row, dy) => {
      row.forEach((pixel, dx) => {
        if (pixel === 1) {
          ctx.fillRect(
            x + dx * scale,
            y + dy * scale,
            scale,
            scale
          );
        }
      });
    });
  }
